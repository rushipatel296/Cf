void solve(){
    int n, m;
    cin >> n >> m;
    vector<int> arr(n), b(m);

    for(int i = 0; i < n; i++) cin >> arr[i];

    for(int i = 0; i < m; i++) {
        cin >> b[i];
        b[i]--;
    }

    sort(all(b));

    vector<ll> pre_sum (n, 0);
    pre_sum[0] = arr[0];

    for(int i = 1; i < n; i++){
        pre_sum[i] = pre_sum[i - 1] + arr[i];
    }
    ll cur_sum = 0;
    for(int i = 0; i < m; i++){
        ll block_sum = pre_sum[b[i]] - ((i - 1 >= 0) ? pre_sum[b[i - 1]] : 0);
        cur_sum += abs(block_sum);
    }
    cur_sum += pre_sum[n - 1] - pre_sum[b[m - 1]];
    cout << cur_sum << endl;
}

