void solve(){
    int n, k;
    cin >> n >> k;

    vector<int> counts(n + 1, 0);
    for (int i = 0; i < n; i++) {
        int x;
        cin >> x;
        counts[x]++;
    }

    int min_L_true = 2e9; 
    bool arseniy_can_win = false;

    for (int v = n; v >= 1; v--) {
        if (counts[v] == 0) continue;

        bool jump_possible = (min_L_true <= v + k);
        bool L_v;

        if (jump_possible) {
            L_v = false;
        } else {
            L_v = (counts[v] % 2 != 0);
        }

        if (L_v) {
            min_L_true = min(min_L_true, v);
        }
        if (!L_v) {
            arseniy_can_win = true;
        }
    }

    if (arseniy_can_win) {
        cout << "YES\n";
    } else {
        cout << "NO\n";
    }
}

