void solve(){
    int n;
    cin >> n;

    vector<pair<int, int>> segments(n);
    for(int i = 0; i < n; i++){
        cin >> segments[i].first >> segments[i].second ; 
    }
    
    ordered_set pbds;
    sort(segments.begin(),segments.end());

    ll count = 0;

    for(int i = 0; i < n; i++){
        int cur_b = segments[i].second;

        int smaller_count = pbds.order_of_key(cur_b); //return count of elements smaller that cur_b in the pbds
        int total_count = pbds.size();
        int greater_count = total_count - smaller_count;

        count += greater_count;
        //insert cur_b
        pbds.insert(cur_b);
    }

    cout << count << endl;
}

